export function d(e, t) {
  (e = [e[0] >>> 16, 65535 & e[0], e[1] >>> 16, 65535 & e[1]]),
    (t = [t[0] >>> 16, 65535 & t[0], t[1] >>> 16, 65535 & t[1]]);
  var n = [0, 0, 0, 0];
  return (
    (n[3] += e[3] + t[3]),
    (n[2] += n[3] >>> 16),
    (n[3] &= 65535),
    (n[2] += e[2] + t[2]),
    (n[1] += n[2] >>> 16),
    (n[2] &= 65535),
    (n[1] += e[1] + t[1]),
    (n[0] += n[1] >>> 16),
    (n[1] &= 65535),
    (n[0] += e[0] + t[0]),
    (n[0] &= 65535),
    [(n[0] << 16) | n[1], (n[2] << 16) | n[3]]
  );
}